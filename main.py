release = b'\x00\x00\x00\x00\x00\x00\x00\x00'

def write_key(str):
    for i in str:
        if i in keys:
            with open('/dev/hidg0', 'rb+') as fd:
                fd.write(keys[i])
                time.sleep(0.005)
                fd.write(release)

def send_combo(*keys):
    modifier = 0
    keycodes = []

    for key in keys:
        key = key.upper()

        # Modifier
        if key in MODIFIERS:
            modifier |= MODIFIERS[key]

        # Normal key
        else:
            actual = key.lower() if len(key) == 1 else key

            if actual not in KEYS:
                raise ValueError(f"Unknown key: {key}")

            keycodes.append(KEYS[actual])

    # Max 6 simultaneous keys
    while len(keycodes) < 6:
        keycodes.append(0x00)

    report = bytes([
        modifier,
        0x00,
        *keycodes[:6]
    ])

    with open('/dev/hidg0', 'wb') as fd:
        fd.write(report)
        time.sleep(0.05)
        fd.write(release)

